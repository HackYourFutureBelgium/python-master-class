# A variable : is a container to store values

first_name = 'John'

# Data types in Python

# 1. Integers (int): These are whole numbers without a decimal point.

x = 5
y = -10

# 2. Floating-Point Numbers (float): These are numbers with a decimal point.

pi = 3.14159
temperature = 98.6
 
# 3. Strings (str): Text data enclosed in single or double quotes.

name = "John"
message = 'Hello, World!'

# 4. Booleans (bool): Represents True or False values.
is_sunny = True
is_raining = False
 
# 5. Lists: Ordered collections of items.

fruits = ["apple", "banana", "cherry"]
numbers = [1, 2, 3, 4, 5]
 

# 6. Tuples: Similar to lists but immutable (cannot be changed).

coordinates = (3, 4)
colors = ('red', 'green', 'blue')


# 7. Dictionaries (dict): Key-value pairs.

person = {'name': 'Alice', 'age': 30, 'city': 'Brussels'}

# 8. Sets: Unordered collections of unique items.

unique_numbers = {1, 2, 3, 4, 5}
unique_letters = {'a', 'b', 'c'}


# 9. NoneType (None): Represents the absence of a value.

empty_value = None
  

# 10. Bytes and Bytearrays: Used to represent binary data.

binary_data = b'\x41\x42\x43'


# 11. Complex Numbers: Combinations of real and imaginary parts.

complex_num = 3 + 4j
  
# 12. Custom Objects: You can create your own data types by defining classes.
class Person:
    def __init__(self, name, age):
        self.name = name
        self.age = age

alice = Person("Alice", 30)
 

